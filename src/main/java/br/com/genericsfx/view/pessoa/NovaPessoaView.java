    private void salvarUsuario(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarUsuario
        if (!this.isUpdate) {
            pessoa = new Pessoa();
        }
        pessoa.setNome(this.campoNome.getText());
        pessoa.setCpf(this.campoCpf.getText());
        pessoa.setEmail(this.campoEmail.getText());
        pessoa.setProfissao(campoProfissao.getText());
        PessoaDao.getInstance().save(pessoa);
        setVisible(false);
    }//GEN-LAST:event_salvarUsuario

    public void preecherCampos() {
        this.campoNome.setText(pessoa.getNome());
        this.campoCpf.setText(pessoa.getCpf());
        this.campoEmail.setText(pessoa.getEmail());
        this.campoProfissao.setText(pessoa.getProfissao());
    }

